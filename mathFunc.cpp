#include <iostream>
#include <cmath>

using namespace std;

int main(){
    //std::floor
    cout << floor(2.34) << endl;
    //std::ceil
    cout << ceil(2.34) << endl;
    //std::abs
    cout << abs(-134) << endl;
    //std::cos
    cout << cos(10) << endl;
    //std::sin
    cout << sin(10) << endl;
    //std::tan
    cout << tan(10) << endl;
    //std::exp -> e^x
    cout << exp(1) << endl;
    //std::log
    cout << log(20) << endl;
    //std::sqrt
    cout << sqrt(49) << endl;
    //std::pow
    cout << pow(3,4) << endl;
    //std::round
    cout << round(32.4324) << endl;
}