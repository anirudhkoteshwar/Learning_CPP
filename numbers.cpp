#include <iostream>
using namespace std;

int main(){
    // integers take 4 bytes
    //
    int n1 = 15; //decimal
    int n2 = 017; //octal
    int n3 = 0xEF14; //hexadecimal
    int n4 = 0b1101101; //binary
    cout << n1 << endl;
    cout << n2 << endl;
    cout << n3 << endl;
    cout << n4 << endl;
}